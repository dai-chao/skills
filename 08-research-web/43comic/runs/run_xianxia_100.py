def log(msg: str) -> None:
    line = msg.rstrip()
    print(line, flush=True)
    LOG_PATH.parent.mkdir(parents=True, exist_ok=True)
    with LOG_PATH.open("a", encoding="utf-8") as f:
        f.write(line + "\n")


def load_json(path: Path):
    with path.open(encoding="utf-8") as f:
        return json.load(f)


def save_json(path: Path, data) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(".tmp")
    with tmp.open("w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")
    tmp.replace(path)


def api_request(method: str, url: str, token: str, body=None, timeout: int = 90):
    data = None
    headers = {"X-Comic-Token": token}
    if body is not None:
        data = json.dumps(body, ensure_ascii=False).encode("utf-8")
        headers["Content-Type"] = "application/json; charset=utf-8"
    req = urllib.request.Request(url, data=data, headers=headers, method=method)
    try:
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            raw = resp.read().decode("utf-8")
            return json.loads(raw) if raw else {}
    except urllib.error.HTTPError as e:
        raw = e.read().decode("utf-8", errors="replace")
        try:
            return json.loads(raw)
        except Exception:
            return {"error": raw or str(e), "httpStatus": e.code}


def ensure_run() -> dict:
    if RUN_PATH.exists():
        run = load_json(RUN_PATH)
        if len(run.get("episodes", [])) == 100:
            return run
    episodes = []
    assert len(EPISODES) == 100, f"need 100 stories, got {len(EPISODES)}"
    for i, (title, story) in enumerate(EPISODES, 1):
        episodes.append({"n": i, "title": title, "storyText": story})
    run = {
        "series": "碎星海仙侠志",
        "worldBible": WORLD,
        "params": PARAMS,
        "episodes": episodes,
        "jobs": [],
    }
    save_json(RUN_PATH, run)
    return run


def job_map(run: dict) -> dict:
    return {j.get("n"): j for j in run.get("jobs", []) if "n" in j}


def poll_job(api_base: str, token: str, job_id: str, max_attempts: int = 90) -> dict:
    for i in range(1, max_attempts + 1):
        url = f"{api_base}/comic.getJob?{urllib.parse.urlencode({'jobId': job_id})}"
        resp = api_request("GET", url, token)
        status = resp.get("status") or ""
        stage = resp.get("stage") or ""
        detail = str(resp.get("detail") or "")[:60]
        log(f"  [poll {i}] status={status} stage={stage} detail={detail}")
        if status in ("done", "failed"):
            return resp
        time.sleep(8)
    return {"status": "timeout", "jobId": job_id}


def main() -> int:
    if not CRED_PATH.exists():
        log("缺少凭证，请先激活")
        return 1
    cred = load_json(CRED_PATH)
    token = cred["comicToken"]
    api_base = cred["apiBase"].rstrip("/")
    run = ensure_run()
    done_map = job_map(run)

    log(f"=== start series={run['series']} episodes={len(run['episodes'])} ===")
    ok = fail = skip = 0

    for ep in run["episodes"]:
        n = ep["n"]
        title_hint = ep["title"]
        prev = done_map.get(n)
        if prev and prev.get("status") == "done" and prev.get("imageUrl"):
            skip += 1
            log(f"SKIP #{n} {title_hint} already done")
            continue

        log(f"===== #{n}/100 {title_hint} submit =====")
        body = dict(PARAMS)
        body["storyText"] = ep["storyText"]
        submit = api_request("POST", f"{api_base}/comic.generate", token, body)
        log(f"submit: {json.dumps(submit, ensure_ascii=False)}")
        job_id = submit.get("jobId") or (submit.get("result") or {}).get("data", {}).get("jobId")
        if not job_id:
            entry = {"n": n, "title": title_hint, "status": "submit_failed", "submit": submit}
            run["jobs"] = [j for j in run["jobs"] if j.get("n") != n] + [entry]
            save_json(RUN_PATH, run)
            fail += 1
            log(f"SUBMIT_FAILED #{n}")
            time.sleep(2)
            continue

        final = poll_job(api_base, token, job_id)
        status = final.get("status") or ""
        out_title = final.get("title") or title_hint
        image_url = final.get("imageUrl") or ""
        detail = final.get("detail") or ""
        entry = {
            "n": n,
            "title": out_title,
            "jobId": job_id,
            "status": status,
            "imageUrl": image_url,
            "detail": detail,
        }
        run["jobs"] = [j for j in run["jobs"] if j.get("n") != n] + [entry]
        save_json(RUN_PATH, run)
        done_map[n] = entry

        if status == "done":
            ok += 1
            log(f"DONE #{n} {out_title} {image_url[:80]}")
        else:
            fail += 1
            log(f"FAIL #{n} status={status} detail={detail}")

        if (ok + fail) % 10 == 0:
            log(f"=== progress ok={ok} fail={fail} skip={skip} processed={ok+fail} ===")

        time.sleep(1)

    log(f"ALL_FINISHED ok={ok} fail={fail} skip={skip}")
    summary = {
        "ok": ok,
        "fail": fail,
        "skip": skip,
        "doneJobs": sum(1 for j in run["jobs"] if j.get("status") == "done"),
        "runPath": str(RUN_PATH),
    }
    print(json.dumps(summary, ensure_ascii=False, indent=2), flush=True)
    return 0 if fail == 0 else 2


if __name__ == "__main__":
    sys.exit(main())
